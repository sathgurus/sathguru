class swap
{
 public static void main(String args[])
 {
	int a,b,temp;
        a=29;
	b=45;
	System.out.println("Before swap A :"+a);
	System.out.println("Before swap B :"+b);
	temp=a;
	a=b;
	b=temp;
	System.out.println("After Swap A :"+a);
	System.out.println("After swap B :"+b);
 }
}
	